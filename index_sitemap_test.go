func TestIndexSitemap(t *testing.T) {
	t.Run("Success add url", func(t *testing.T) {
		indexSitemap := NewSitemap()

		err := indexSitemap.Add(&URL{
			Loc:        "/test",
			ChangeFreq: Always,
			Priority:   0,
		})

		assert.NoError(t, err)
		assert.Equal(t, 1, indexSitemap.countURL)
	})

	t.Run("Error max add files", func(t *testing.T) {
		indexSitemap := NewSitemap()

		var err error

		for i := 0; i < maxUrls+1; i++ {
			err = indexSitemap.Add(&URL{
				Loc:        "/test",
				ChangeFreq: Always,
				Priority:   0,
			})
		}

		assert.Equal(t, ErrorAddEntity, err)
	})

	t.Run("Get xml", func(t *testing.T) {
		indexSitemap := NewSitemap()

		var err error
		times, _ := time.Parse("2006-01-02", "2011-01-19")

		err = indexSitemap.Add(&URL{
			Loc:        "/test",
			LastMod:    XMLTime(times),
			ChangeFreq: Always,
			Priority:   0,
		})
		rr := indexSitemap.GetXML()

		assert.NoError(t, err)
		assert.Equal(t, testData, rr)
	})
}
